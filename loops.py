# For loop

meals = ['apples', 'bread', 'cheese', 'milk']

for meal in meals:
    if meal == 'milk':
        print('You have to buy this! ' + meal)
    print(meal)

for number in range(1, 8):
    print(number)

for letter in 'Hello':
    print(letter)


# While loop

count = 4

while count <= 10:
    print(count)
    count += 1