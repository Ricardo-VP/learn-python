# Strings
print("Hello World")
print('Hello World')
print('''Hello World''')
print("""Hello World""")
print('Bye ' + 'World')
print(type('Hello World'))

# Integer
print(type(30))
# Float
print(type(30.5))

# Boolean
print(type(True))
print(type(False))

# List (mutable)
print(type([10, 'Hello', True, 20.5]))

# Tuples (inmutable)
print(type((10, 'Hello', True, 20.5)))

# Dictionaries
print(type(
    {
        "name": "Ricardo",
        "lastname": "Vaca",
        "age": 20
    }
))
